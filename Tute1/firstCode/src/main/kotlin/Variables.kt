fun main() {
    val intVal = 1 // integer variable
    val longVal = 1L // long integer
    val byteVal: Byte = 1 // if we initializing the byte we have to set it into byte variable type

    // printing output
    println("Integer Value = $intVal")
    println("Long Value = $longVal")
    println("Byte Value = $byteVal")

}